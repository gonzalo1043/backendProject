export class Product {
    constructor({ id, title, description, price, status, category, thumbnail, code, stock }) {
        this.id = id;
        this.title = title;
        this.description = description;
        this.price = price;
        this.status = status;
        this.category = category;
        this.thumbnail = thumbnail;
        this.code = code;
        this.stock = stock;
    }
}
